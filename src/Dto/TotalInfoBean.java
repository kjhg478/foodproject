package Dto;

public class TotalInfoBean {
	private String todayLogin;
	private String todayGames;
	private String totalStores;
	private String totalReviews;
	
	public String getTodayLogin() {
		return todayLogin;
	}
	public void setTodayLogin(String todayLogin) {
		this.todayLogin = todayLogin;
	}
	public String getTodayGames() {
		return todayGames;
	}
	public void setTodayGames(String todayGames) {
		this.todayGames = todayGames;
	}
	public String getTotalStores() {
		return totalStores;
	}
	public void setTotalStores(String totalStores) {
		this.totalStores = totalStores;
	}
	public String getTotalReviews() {
		return totalReviews;
	}
	public void setTotalReviews(String totalReviews) {
		this.totalReviews = totalReviews;
	}
	
	
	
}
